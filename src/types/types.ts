// Возможные типы эффектов (расширяемый список)
export type StepEffectType =
  | "highlight"      // Выделение элементов (pivot, найденный, etc)
  | "compare"        // Сравнение двух или нескольких элементов
  | "swap"           // Перестановка элементов
  | "mark"           // Пометить элемент специальным статусом
  | "partition"      // Принадлежность к подмножеству (после деления)
  | "custom";        // Для уникальных действий/визуализаций

// Эффект-описатель визуального действия на шаге
export interface StepEffect {
  type: StepEffectType;
  indices?: number[];    // Индексы элементов, к которым применяется эффект
  label?: string;        // Любая краткая подпись ("pivot", "found", "left" ...)
  color?: string;        // Рекомендуемый цвет (если хотите управлять вне визуализатора)
  value?: number | number[]; // Любое доп. значение (например, результат, новое значение)
  path?: number[];       // Для вложенности в рекурсивных структурах
  [key: string]: any;    // Максимальная расширяемость под другие задачи
}

// Описание одного шага алгоритма для визуализации
export interface AlgorithmStep {
  stepId: number;           // Уникальный номер шага
  algorithm: string;        // Имя алгоритма ("quickSort", "binarySearch" ...)
  depth: number;            // Глубина (для рекурсивных/итеративных визуализаций)
  data: {
    array: number[];        // Текущее содержимое массива/набора значений
    [key: string]: any;     // Остальные поля — для отрисовки или отладки (например, target, memo, etc)
  };
  effects: StepEffect[];    // Все визуальные эффекты для этого шага
  path?: number[];          // Путь вложенности (рекурсия, дерево) — например, [0,1,1]
}
